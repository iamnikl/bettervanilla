		<h1>What are Modpacks</h1>
		<h1 />
	</hgroup>
	<p style="padding: 0rem 5rem;">
		Minecraft is a sandbox game that allows players to explore and create their own worlds. However,
		one of the reasons for its enduring popularity is the ability for players to modify the game
		using mods. Mods, short for modifications, are user-made additions to the game that can range
		from small tweaks to major overhauls of gameplay.
	</p>
	<hgroup style="padding-top: 4rem;">
		<h1>What can modpacks do?</h1>
		<h1 />
	</hgroup>
	<p style="padding: 0rem 5rem;">
		Mods can change Minecraft gameplay in many different ways. Some mods may add new content, such
		as new items, blocks, or mobs. These additions can range from simple changes to more complex
		ones that introduce entirely new systems. For example, a mod may add a new type of weapon or
		tool, or it may add new biomes or dimensions to explore. In some cases, mods may even introduce
		entirely new game mechanics, such as new ways to craft items or new systems for managing
		resources.
		<br /><br />
		Other mods may focus on changing the mechanics of the game. This can include altering the way certain
		features work, introducing new systems altogether, or changing the way the game is played. For example,
		a mod may change the way mobs spawn or behave, or it may introduce new crafting systems that require
		players to use different materials or methods. Some mods may even change the fundamental rules of
		the game, such as making certain blocks indestructible or introducing new types of damage.
		<br /><br />
		Mods can also change Minecraft's graphics and visual effects. This can include enhancing Minecraft's
		graphics and performance, allowing players to run the game more smoothly and with more detailed textures
		and lighting. Other mods may add new visual effects like weather or particle effects, or change the
		way the game looks entirely with new textures and models. Some mods may even add new music or sound
		effects to enhance the atmosphere of the game.
	</p>
